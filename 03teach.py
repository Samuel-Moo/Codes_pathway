sidesquare = float(input("How long is the side of the square? "))
square = sidesquare ** 2 
print(f"The area of the square is: {square}.")
length = float(input("What is the length of the rectangle? "))
height = float(input("What is the height of the rectangle? "))
rectangle = length * height 
print(f"The area of the rectangle is {rectangle}.")
radius = float(input("What is the radius of the circle? "))
import math
circle = (radius ** 2) * math.pi
print(f"The area of the circle is: {circle}.")
